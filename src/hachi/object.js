/**
 * 객체 생성 javascript
 */

//object() 를 이용해서 foo 빈 객체 생성

var foo = {
    name : 'foo'
    ,age : 30
    ,gender : 'male'
    ,major : 'computer sciences'
};

//foo 객체 프로퍼티 생성
foo.name = 'foo';
foo.age = 30;
foo.gender = 'male';

foo.major = 'electronics engineering';
foo['full-Name'] = 'foo bar';